	p=s;
	for (count=0; count<=2; count++)
	{
	    q=strchr(p, '.');
	    if (q) *q='\0';
	    tokens[count]=p;
	    if (q) p=q+1;
	}
	MajVer2=atoi(tokens[0]);
	MinVer2=atoi(tokens[1]);
	PthNo2=atoi(tokens[2]);

