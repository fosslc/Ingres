CPreInstallation::~CPreInstallation()
{
    for(int i=0; i<m_Installations.GetSize(); i++)
    {
	CInstallation *inst=(CInstallation *) m_Installations.GetAt(i);

	if(inst)
	    delete inst;
    }
    m_Installations.RemoveAll();
}

