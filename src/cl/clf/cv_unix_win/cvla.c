/*{
** Name: CVla8      - Convert i8 to ASCII.
**
** Description:
**	i8 to ASCII
**
**	Converts the i8 integer num to ASCII and stores the
**	result in string, which is assumed to be large enough.
**
**	NOTE: Identical algorithm to CVla apart from datatype
**	      of val & tmp and size of buf.
**
** Inputs:
**	var		number to convert.
**
** Output:
**	out		ASCII buffer to fill in.
**
**      Returns:
**	    none. (VOID)
**
** History:
**	12-May-2008 (kiria01) b120372
**	    Reworked to use a faster algorithm that reduces the divide costs
**	    and byte memory access.
*/
VOID
CVla8(register i8 val, register char *out)
{
    if (val == 0)
    {
	/* Short circuit 0 as freqent and later we would
	** remove leading zeros */
	*out++ = '0';
    }
    else
    {
	/* 24 byte buffer to hold result - aligned size so that lp_p will be aligned */
	LUTYPE buf[24/sizeof(LUTYPE)];
	/* Pointer to end of temp write store */
	register LUTYPE *lu_p = buf + sizeof(buf)/sizeof(LUTYPE);

	{
	    register i8 tmp;

	    /* Handle negative if needed - in the process
	    ** we will convert the sign to work with the
	    ** remaining loop*/
	    if (val < 0)
	    {
		/* Save sign directly to output buffer - we'll append later */
		*out++ = '-';
		/* Split the number into div and remainder
		** The order of the calculations is important
		** to preserve the sign without overflow
		*/
		tmp = val;
		val /= -LUSCALE;
		tmp += LUSCALE*val;
		/* Store this segment of the result */
		*--lu_p = LUTAB[-tmp];
	    }

	    while (tmp = val)
	    {
		/* Split the number into div and remainder */
		val /= LUSCALE;
		/* Store this segment of the result */
		*--lu_p = LUTAB[tmp-LUSCALE*val];
	    }
	}

	/* The formed number is pointed at by lu_p which
	** may have leading zeros that need loosing which
	** will break alignment as would any leading '-'
	** so the rest is a simple copy to output
	*/
	{
	    register char *p = (char*)lu_p;
	    /* point lu_p back to end of buffer */
	    lu_p = buf + sizeof(buf)/sizeof(LUTYPE);
	    /* Skip leading zeros */
	    while (*p == '0')
		p++;
	    /* Copy is delimited by address instead of
	    ** incurring a scan for a null char.
	    */
	    do
	    {
		*out++ = *p++;
	    } while ((LUTYPE*)p < lu_p);
	}
    }
    *out++ = EOS;
}

