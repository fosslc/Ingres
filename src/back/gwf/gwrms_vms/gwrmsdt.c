/*{
** Name: IIudadt_register	- Add RMS datatypes and coercions to gateway
**
** Description:
**
** Inputs:
**      add_cb                      Pointer to info add cb
**
** Outputs:
**      *add_cb			    We set this to our rms_reg_cb to add the
**				    RMS types and coercions
**
**	Returns:
**	    DB_STATUS
**	Exceptions:
**	    none
**
** Side Effects:
**	    rms_fis[].fid_id is belatedly initialized dynamically.
**
** History:
**	26-mar-90 (jrb)
**	    Created for RMS Gateway project.
**	22-jun-90 (edwin)
**	    Initialize rms_fis[].fid_id dynamically here.
**      21-sep-93 (schang)
**          change ADD_FISTART to ADD_USER_FISTART because the first one
**          is replaced by the latter by stevet to support class object.
[@history_template@]...
*/
DB_STATUS
IIudadt_register
(
    ADD_DEFINITION   **add_cb,
    ADD_CALLBACKS    *callbacks
)
{
    i4	    j;
    i4	    id = ADD_USER_FISTART;

    /*
    **  To avoid having to rewrite the initialization of the fid_id field
    **  every time we modify the definition of rms_fis, do it dynamically here.
    */
    for (j = 0; j < rms_reg_cb.add_fi_cnt; j++)
	rms_fis[j].fid_id = id++;

    rms_reg_cb.add_count = rms_reg_cb.add_dt_cnt +
			   rms_reg_cb.add_fo_cnt +
			   rms_reg_cb.add_fi_cnt;
    *add_cb = &rms_reg_cb;

    return(E_DB_OK);
}

