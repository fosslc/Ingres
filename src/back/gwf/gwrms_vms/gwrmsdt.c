/**
**
**  Name: GWRMSDT.C - Code for adding all RMS types as user-defined types
**
**  Description:
**	For the RMS gateway, we add all supported RMS datatypes as
**	user-defined datatypes (unless there is an INGRES-compatible datatype
**	for it, in which case we just use the INGRES datatype).
**
**	This file constructs the block for SCF to add the new datatypes.
**
**	    RMSGW_INT		    1,2,4,8, or 16 byte signed integer
**	    RMSGW_UNSINT	    1,2,4,8, or 16 byte unsigned integer
**	    RMSGW_FFLOAT	    4 byte f-float
**          RMSGW_DFLOAT            8 byte d-float
**          RMSGW_GFLOAT            8 byte g-float
**          RMSGW_HFLOAT            16 byte h-float
**          RMSGW_SFLOAT            IEEE 4 byte s-float (alpha)
**          RMSGW_TFLOAT            IEEE 8 byte t-float (alpha)
**	    
**	    RMSGW_BL_FIXSTR	    Blank-padded fixed-length string
**	    RMSGW_NL_FIXSTR	    Null-padded fixed-length string
**	    RMSGW_BL_VARSTR	    Blank-padded "varying-length" string
**	    RMSGW_NL_VARSTR	    Null-padded "varying-length" string
**	    
**	    RMSGW_VAXDATE	    VAX date (8 bytes)
**	    
**	    RMSGW_PACKED_DEC	    Packed decimal
**	    RMSGW_LEADING_NUM	    Leading numeric string
**	    RMSGW_ZONED_NUM	    Zoned numeric string
**	    RMSGW_UNS_NUM	    Unsigned numeric string
**	    RMSGW_OVR_NUM	    Overpunched numeric string
**	    RMSGW_TRL_NUM	    Trailing separate numeric string
**          RMSGW_IMAGINE           repeating group phantom data type
**
**
**  History:
**	21-mar-90 (jrb)
**	    Created.
**	14-jun-90 (jrb)
**	    Added blank-padded fixed strings.
**	22-jun-90 (edwin)
**	    Add RMSGW_NATFLOAT.
**	26-jun-90 (linda)
**	    Add RMSGW_BL_VARSTR, RMSGW_NL_VARSTR string types.
**	7-sep-90 (linda)
**	    Added support for "trailing separate numeric" datatype.  This type
**	    is a string type, one byte per ascii digit, with a separate sign
**	    byte at the end.  It is generated by COBOL PIC statements.  Its
**	    internal type definition is RMSGW_TRL_NUM.
**      17-dec-1992 (schang)
**          prototype
**      27-Jul-1993 (fred)
**          Modified OME declarations to match new add.h changes for 
**          large object support.
**      25-may-1993 (schang)
**          add RMSGW_IMAGE data type for repeating group support
**      21-sep-93 (schang)
**          change ADD_FISTART to ADD_USER_FISTART because the first one
**          is replaced by the latter by stevet to support class object.
**      26-feb-97 (schang)
**          modify floating point support, now we have explicit RMS floating
**          data type f-,d-,g-,h-,s-,t- types
**      31-dec-97 (chash01)
**          add support for byte datatypes
**      03-mar-1999 (chash01)
**          integrate all changes since ingres 6.4.
**      28-jun-1999 (chash01)
**          add one more member into rms_dts
**      21-mar-2000 (chash01)
**          somehow ingres deciaml datatype support is missing.
**	28-feb-2001	(kinte01)
**	    Bug 103393 - removed nat, longnat, u_nat, & u_longnat
**	    from RMS GW code as the use is no longer allowed
**/

